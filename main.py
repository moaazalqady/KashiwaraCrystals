def generate_domain_elements(domain_tree):
    """
    Recursively generates all elements of a tensor product domain.
    domain_tree should be a list (a base crystal) or a binary tuple of sub-trees.
    Example: ((B_1, B_2), B_2) yields elements of (B_1 ⊗ B_2) ⊗ B_2.
    """
    # Base Case: It is a fundamental crystal list
    if isinstance(domain_tree, list):
        for node in domain_tree:
            yield node

    # Recursive Case: It is a tensor product of two sub-domains
    elif isinstance(domain_tree, tuple) and len(domain_tree) == 2:
        left_tree, right_tree = domain_tree
        for l_node in generate_domain_elements(left_tree):
            for r_node in generate_domain_elements(right_tree):
                yield TensorNode(l_node, r_node)
    else:
        raise ValueError("domain_tree must be a list (crystal) or a binary tuple of sub-trees.")


def evaluate_morphism(morphism, domain_tree, name=None, print_zeros=True):

    if name:
        print(f"\n[ Evaluating Morphism: {name} ]")
    print("-" * 50)

    for element in generate_domain_elements(domain_tree):
        result = morphism(element)

        # Determine if the result is "Zero" (either None or an empty FormalSum)
        if hasattr(result, 'terms'):  # It's a FormalSum
            is_zero = not bool(result.terms)
        else:  # It's a standard CrystalMorphism mapping
            is_zero = (result is None)

        if not is_zero or print_zeros:
            print(f"  {element}  |--->  {result}")


