class CrystalMorphism:
    def __init__(self, domain_nodes, mapping_dict, lie_algebra):
        self.domain = list(domain_nodes)
        self.mapping = {n: mapping_dict.get(n, None) for n in domain_nodes}
        self.lie_algebra = lie_algebra

    def __call__(self, node):
        return self.mapping.get(node, None)

    def compose(self, other):
        if isinstance(other, LinearMorphism):
            return LinearMorphism(self).compose(other)

        # If the FIRST morphism is lazy, the whole chain must become lazy
        if getattr(self, 'domain', None) is None:
            return StructuralMorphism(lambda x: other(self(x)))

        # If the FIRST morphism has a fixed domain, evaluate the whole chain strictly
        new_mapping = {}
        for x in self.domain:
            fx = self(x)
            new_mapping[x] = other(fx) if fx is not None else None

        return CrystalMorphism(self.domain, new_mapping,
                               getattr(self, 'lie_algebra', getattr(other, 'lie_algebra', None)))

    def tensor(self, other):
        if isinstance(other, LinearMorphism): return LinearMorphism(self).tensor(other)
        if getattr(self, 'domain', None) is None or getattr(other, 'domain', None) is None:
            def lazy_eval(x):
                if x is None: return None
                fx = self(x.b1)
                gx = other(x.b2)
                if fx is None or gx is None: return None
                return TensorNode(fx, gx)
            return StructuralMorphism(lazy_eval)

        domain_nodes = [TensorNode(b1, b2) for b1 in self.domain for b2 in other.domain]
        new_mapping = {}
        for x in domain_nodes:
            fx = self(x.b1)
            gx = other(x.b2)
            if fx is not None and gx is not None: new_mapping[x] = TensorNode(fx, gx)
            else: new_mapping[x] = None
        return CrystalMorphism(domain_nodes, new_mapping, self.lie_algebra)

    # ===== LINEAR PROMOTION OVERLOADS =====
    def __add__(self, other): return LinearMorphism(self) + other
    def __sub__(self, other): return LinearMorphism(self) - other
    def __mul__(self, scalar): return LinearMorphism(self) * scalar
    def __rmul__(self, scalar): return self.__mul__(scalar)
    def __neg__(self): return LinearMorphism(self) * (-1)

    def __repr__(self):
        lines = [f"Crystal Morphism (Domain size: {len(self.domain)}):"]
        for d in self.domain: lines.append(f"  {d}  |--->  {self.mapping.get(d, '0')}")
        return "\n".join(lines)

class StructuralMorphism(CrystalMorphism):
    def __init__(self, mapping_func):
        self.mapping_func = mapping_func
        self.domain = None
        self.lie_algebra = None

    def __call__(self, node):
        if node is None: return None
        return self.mapping_func(node)

    def __repr__(self):
        return "[Polymorphic Structural Morphism - Evaluates Lazily]"


