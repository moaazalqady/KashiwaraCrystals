def build_commutor(B_i, B_j, lie_algebra):
    """
    Constructs the canonical crystal commutor isomorphism
    from B_i ⊗ B_j to B_j ⊗ B_i.
    """
    domain_nodes = [TensorNode(a, b) for a in B_i for b in B_j]
    codomain_nodes = [TensorNode(b, a) for b in B_j for a in B_i]

    # 1. Isolate the highest weight vectors of both tensor spaces
    hw_domain = [n for n in domain_nodes if all(n.e(i) is None for i in lie_algebra.index_set)]
    hw_codomain = [n for n in codomain_nodes if all(n.e(i) is None for i in lie_algebra.index_set)]

    mapping = {}
    queue = []

    # 2. Match the highest weight components by their strict weights
    for d_hw in hw_domain:
        # Since fundamental tensor products in Type A are multiplicity-free, this is unique
        matching_c_hw = next((c for c in hw_codomain if c.wt() == d_hw.wt()), None)

        if matching_c_hw is None:
            raise ValueError(f"Weight mismatch! No highest weight of wt {d_hw.wt()} found in codomain.")

        mapping[d_hw] = matching_c_hw
        queue.append(d_hw)

    visited = set(queue)

    # 3. Extend functorially to the rest of the connected components
    while queue:
        curr = queue.pop(0)
        for i in lie_algebra.index_set:
            nxt = curr.f(i)
            if nxt is not None and nxt not in visited:
                visited.add(nxt)
                # Ensure the map strictly commutes with \tilde{f}_i
                mapping[nxt] = mapping[curr].f(i)
                queue.append(nxt)

    return CrystalMorphism(domain_nodes, mapping, lie_algebra)


