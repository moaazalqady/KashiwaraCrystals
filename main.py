def generate_An_category(n):
    cartan = LieAlgebra(f'A{n}')

    # 1. Generate Fundamental Crystals B_0, B_1, ..., B_n
    B = {}

    # Trivial Crystal B_0
    B[0] = generate_crystal(cartan, tuple([0] * n))

    # Fundamental Crystals B_i
    for i in range(1, n + 1):
        hw = tuple([1 if k == i - 1 else 0 for k in range(n)])
        B[i] = generate_crystal(cartan, hw)

    print(f"[✓] Generated {n + 1} Fundamental Crystals (B_0 to B_{n})")

    # 2. building inverses for the left and right unitors
    trivial_hw = B[0][0]
    lu_inv = StructuralMorphism(lambda n: TensorNode(trivial_hw, n))
    ru_inv = StructuralMorphism(lambda n: TensorNode(n, trivial_hw))

    # 3. Generate Cups and Caps (i + j = 0 mod n+1)
    cup = {}
    cap = {}
    for i in range(n + 1):
        j = (n + 1 - i) % (n + 1)
        # Cup: B_0 -> B_i ⊗ B_j  (Coevaluation)
        cup[(i, j)] = build_inclusion(B[0], B[i], B[j], cartan)
        # Cap: B_i ⊗ B_j -> B_0  (Evaluation)
        cap[(i, j)] = build_projection(B[i], B[j], B[0], cartan)

    print(f"[✓] Generated {len(cup)} Cups and {len(cap)} Caps")

    # 4. Generate Canonical Inclusions and Projections (i = j + k mod n+1)
    Y = {}
    Y_bar = {}
    for j in range(n + 1):
        for k in range(n + 1):
            i = (j + k) % (n + 1)

            # Y_i^{j,k} : B_i -> B_j ⊗ B_k
            Y[(i, j, k)] = build_inclusion(B[i], B[j], B[k], cartan)

            # Y-bar_{j,k}^i : B_j ⊗ B_k -> B_i
            Y_bar[(j, k, i)] = build_projection(B[j], B[k], B[i], cartan)

    print(f"[✓] Generated {len(Y)} Canonical Inclusions (Y)")
    print(f"[✓] Generated {len(Y_bar)} Canonical Projections (Y-bar)")

    # 5. Generate Commutors (B_i ⊗ B_j -> B_j ⊗ B_i)
    comm = {}
    for i in range(n + 1):
        for j in range(n + 1):
            comm[(i, j)] = build_commutor(B[i], B[j], cartan)

    print(f"[✓] Generated {len(comm)} Commutors")
    print(f"==================================================\n")

    return cartan, B, cup, cap, Y, Y_bar, comm, lu_inv, ru_inv

