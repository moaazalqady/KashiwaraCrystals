class FormalSum:
    """Represents a formal linear combination of Crystal Nodes (elements of the free vector space)."""

    def __init__(self, terms=None):
        self.terms = {}
        if terms:
            for node, coeff in terms.items():
                if coeff != 0:
                    self.terms[node] = self.terms.get(node, Fraction(0)) + Fraction(coeff)

    def __add__(self, other):
        res = dict(self.terms)
        for node, coeff in other.terms.items():
            new_coeff = res.get(node, 0) + coeff
            if new_coeff == 0:
                res.pop(node, None)
            else:
                res[node] = new_coeff
        return FormalSum(res)

    def __sub__(self, other):
        return self + (other * -1)

    def __mul__(self, scalar):
        return FormalSum({n: c * Fraction(scalar) for n, c in self.terms.items()})

    def __rmul__(self, scalar):
        return self.__mul__(scalar)

    def __neg__(self):
        return self.__mul__(-1)

    def __repr__(self):
        if not self.terms: return "0"
        parts = []
        for i, (node, c) in enumerate(self.terms.items()):
            if i == 0:
                if c == 1:
                    parts.append(f"{node}")
                elif c == -1:
                    parts.append(f"-{node}")
                else:
                    parts.append(f"{c}*{node}")
            else:
                if c == 1:
                    parts.append(f" + {node}")
                elif c == -1:
                    parts.append(f" - {node}")
                elif c > 0:
                    parts.append(f" + {c}*{node}")
                else:
                    parts.append(f" - {abs(c)}*{node}")
        return "".join(parts)


class LinearMorphism:
    """Promotes a strict/lazy Morphism to operate on and return FormalSums."""

    def __init__(self, base_morphism):
        self.base = base_morphism

    def __call__(self, node_or_sum):
        if node_or_sum is None: return FormalSum()

        # Extend linearly if evaluated on a FormalSum
        if isinstance(node_or_sum, FormalSum):
            res = FormalSum()
            for n, c in node_or_sum.terms.items():
                eval_n = self.base(n)
                if isinstance(eval_n, FormalSum):
                    res += eval_n * c
                elif eval_n is not None:
                    res += FormalSum({eval_n: c})
            return res
        else:
            eval_n = self.base(node_or_sum)
            if isinstance(eval_n, FormalSum): return eval_n
            if eval_n is None: return FormalSum()
            return FormalSum({eval_n: 1})

    def compose(self, other):
        other_lin = other if isinstance(other, LinearMorphism) else LinearMorphism(other)
        return LinearMorphism(StructuralMorphism(lambda x: other_lin(self(x))))

    def tensor(self, other):
        other_lin = other if isinstance(other, LinearMorphism) else LinearMorphism(other)

        def lazy_tensor_eval(x):
            if x is None: return FormalSum()
            fx = self(x.b1)
            gx = other_lin(x.b2)
            res = FormalSum()
            # Extend bilinearly
            for n1, c1 in fx.terms.items():
                for n2, c2 in gx.terms.items():
                    res += FormalSum({TensorNode(n1, n2): c1 * c2})
            return res

        return LinearMorphism(StructuralMorphism(lazy_tensor_eval))

    def __add__(self, other):
        other_lin = other if isinstance(other, LinearMorphism) else LinearMorphism(other)
        return LinearMorphism(StructuralMorphism(lambda x: self(x) + other_lin(x)))

    def __sub__(self, other):
        other_lin = other if isinstance(other, LinearMorphism) else LinearMorphism(other)
        return LinearMorphism(StructuralMorphism(lambda x: self(x) - other_lin(x)))

    def __mul__(self, scalar):
        return LinearMorphism(StructuralMorphism(lambda x: self(x) * scalar))

    def __rmul__(self, scalar):
        return self.__mul__(scalar)

    def __neg__(self):
        return self.__mul__(-1)

    def __repr__(self):
        if getattr(self.base, 'domain', None) is not None:
            lines = [f"Linear Morphism (Domain size: {len(self.base.domain)}):"]
            for d in self.base.domain: lines.append(f"  {d}  |--->  {self(d)}")
            return "\n".join(lines)
        return "[Polymorphic Linear Morphism]"


