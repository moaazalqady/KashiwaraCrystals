#DANGER: The Following only works for type A as it relies on the fact that tensoring crystals of simple weights is multiplicity-free
def build_inclusion(B_in, B_out_1, B_out_2, lie_algebra):
    B_target_tensor = [TensorNode(b1, b2) for b1 in B_out_1 for b2 in B_out_2]
    hw_in = B_in[0]

    targets = [n for n in B_target_tensor if
               n.wt() == hw_in.wt() and all(n.e(m) is None for m in lie_algebra.index_set)]
    if not targets:
        raise ValueError(f"No highest weight node of weight {hw_in.wt()} found in target tensor product.")

    target_hw = targets[0]
    mapping = {hw_in: target_hw}
    queue = [hw_in]
    visited = set([hw_in])

    while queue:
        curr = queue.pop(0)
        for m in lie_algebra.index_set:
            nxt = curr.f(m)
            if nxt is not None and nxt not in visited:
                visited.add(nxt)
                mapping[nxt] = mapping[curr].f(m) if mapping[curr] is not None else None
                queue.append(nxt)

    return CrystalMorphism(B_in, mapping, lie_algebra)

def build_projection(B_in_1, B_in_2, B_target, lie_algebra):
    B_domain_tensor = [TensorNode(b1, b2) for b1 in B_in_1 for b2 in B_in_2]
    hw_target = B_target[0]

    targets = [n for n in B_domain_tensor if
               n.wt() == hw_target.wt() and all(n.e(m) is None for m in lie_algebra.index_set)]
    if not targets:
        raise ValueError(f"No highest weight node of weight {hw_target.wt()} found in domain tensor product.")

    domain_hw = targets[0]
    mapping = {n: None for n in B_domain_tensor}
    mapping[domain_hw] = hw_target
    queue = [domain_hw]
    visited = set([domain_hw])

    while queue:
        curr = queue.pop(0)
        for m in lie_algebra.index_set:
            nxt = curr.f(m)
            if nxt is not None and nxt not in visited:
                visited.add(nxt)
                mapping[nxt] = mapping[curr].f(m) if mapping[curr] is not None else None
                queue.append(nxt)

    return CrystalMorphism(B_domain_tensor, mapping, lie_algebra)


